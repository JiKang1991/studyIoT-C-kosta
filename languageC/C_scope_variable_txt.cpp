/* 변수의 특성에 따른 분류
	- 지역 변수(Local Variable)
	 : 중괄호 내에서 선언되는 변수, 중괄호 내에서만 유효하다
	- 전역 변수(Global Variable)
	 : 함수 내에 선언되지 않는 변수, 정적 변수
	 : 프로그램 어디에서나 접근이 가능한 변수
	 : 특정 영역(함수, 반복문...)에 속하지 않는다.
	 : 프로그램이 종료될 때까지 존재한다.
	 : 같은 이름의 지역 변수에 의해서 가려지기도 한다.(참조시 지역변수 우선) 
	- 정적 변수(Static Variable)
	 : 함수 내부, 외부 모두 선언 가능
	- 레지스터 변수(Register Variable)
	 : 선언에 제한이 많이 따름*/
	 
